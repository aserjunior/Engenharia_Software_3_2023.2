/*
De acordo com o artigo Coesão e Acoplamento em Sistemas Orientados a Objetos,
o conceito de coesão está relacionado entre as responsabilidades e os metodos das classes.
Falando em coesão de classe, elas são dividas em três tipos:
    Coesão de instancia mista:
    Quando a classe tem caracteristicas que são indefinidas para alguns objetos da classe.

    Coesão de domínio misto:
    Quando classes de domínios diferentes(domínio  de arquitetura, domínio do negócio, dentre outros), 
    que podem  ser definidas de forma independente, mantêm alguma dependência.

    Coesão de papel misto:
    Quando classes do mesmo dominio, que podem ser definidas de forma independente, mantêm alguma
    dependência. 

Em relação a coesão de métodos são dividos em três tipos:
    Coesão alternada:
    Quando um método tem varios etapas do comportamento da classe mas, sempre ou vai executar uma parte ou outra,
    dessa forma sempre vai ter uma parte sendo inutilizada.

    Coesão múltipla:
    Quando um método também tem várias etapas mas é o contrario da alternada pois nesse caso,
    um método vai realizar mais de uma etapa.

    Coesão funcional:
    Esse seria o ideal para os métodos pois, nesse caso um método seria responsavel por uma etapa,
    tornando assim o método coeso já que ele realizaria o que está sendo pedido de forma concisa
*/
